#include <iostream>
using namespace std;

char c[125][125];

int main()
{
    int n;
    scanf("%d",&n);
    int N=5+4*n;
    for(int i=1;i<=N;i++)
    {
        for(int j=1;j<=N;j++)
            c[i][j]='.';
    }
    c[N/2+1][N/2+1]='$';    c[N/2+1][N/2]='$';  c[N/2+1][N/2-1]='$';
    c[N/2+1][N/2+2]='$';    c[N/2+1][N/2+3]='$';    c[N/2][N/2+1]='$';
    c[N/2-1][N/2+1]='$';    c[N/2+2][N/2+1]='$';    c[N/2+3][N/2+1]='$';
    for(int i=1;i<=n;i++)
    {
        c[N/2+1-2*i][N/2+1-2*i]='$';    c[N/2+1-2*i][N/2-2*i]='$';  c[N/2+1-2*i][N/2-1-2*i]='$';
        c[N/2-2*i][N/2+1-2*i]='$';  c[N/2-1-2*i][N/2+1-2*i]='$';

        c[N/2+1-2*i][N/2+1+2*i]='$';    c[N/2+1-2*i][N/2+2+2*i]='$';  c[N/2+1-2*i][N/2+3+2*i]='$';
        c[N/2-2*i][N/2+1+2*i]='$';  c[N/2-1-2*i][N/2+1+2*i]='$';

        c[N/2+1+2*i][N/2+1-2*i]='$';    c[N/2+1+2*i][N/2-2*i]='$';  c[N/2+1+2*i][N/2-1-2*i]='$';
        c[N/2+2+2*i][N/2+1-2*i]='$';  c[N/2+3+2*i][N/2+1-2*i]='$';

        c[N/2+1+2*i][N/2+1+2*i]='$';    c[N/2+1+2*i][N/2+2+2*i]='$';  c[N/2+1+2*i][N/2+3+2*i]='$';
        c[N/2+2+2*i][N/2+1+2*i]='$';  c[N/2+3+2*i][N/2+1+2*i]='$';
    }
    for(int i=1;i<=n;i++)
    {
        for(int j=N/2+1-2*i;j<=N/2+1+2*i;j++)
        {
            c[N/2-1-2*i][j]='$';
            c[N/2+3+2*i][j]='$';
            c[j][N/2-1-2*i]='$';
            c[j][N/2+3+2*i]='$';
        }
    }
    for(int i=1;i<=N;i++)
    {
        for(int j=1;j<=N;j++)
            printf("%c",c[i][j]);
        printf("\n");
    }
    return 0;
}
